package com.aaa.io;

public class Root {
    private int code;

    private int cid;

    private String userip;

    private Data data;

    public void setCode(int code) {
        this.code = code;
    }

    public int getCode() {
        return this.code;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }

    public int getCid() {
        return this.cid;
    }

    public void setUserip(String userip) {
        this.userip = userip;
    }

    public String getUserip() {
        return this.userip;
    }

    public void setData(Data data) {
        this.data = data;
    }

    public Data getData() {
        return this.data;
    }
}
